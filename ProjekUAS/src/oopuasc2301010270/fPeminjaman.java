    txNAMA.setText(tblpeminjaman_buku.getValueAt(tblpeminjaman_buku.getSelectedRow(), 2).toString());
    txKODE.setText(tblpeminjaman_buku.getValueAt(tblpeminjaman_buku.getSelectedRow(), 3).toString());
    txJUDUL.setText(tblpeminjaman_buku.getValueAt(tblpeminjaman_buku.getSelectedRow(), 4).toString());
    
