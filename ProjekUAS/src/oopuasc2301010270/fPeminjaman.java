    private void lsdtpeminjaman() throws SQLException {
        Connection cnn = koneksi ();
        
        DTM.getDataVector().removeAllElements();
        DTM.fireTableDataChanged();
        
        if( !cnn.isClosed()){
            PreparedStatement PS = cnn.prepareStatement("SELECT *FROM peminjaman_buku;");
            ResultSet RS = PS.executeQuery();
            while( RS.next() ){
                Object[] dtpeminjaman = new Object[7];
                dtpeminjaman[0] = RS.getString("ID");
                dtpeminjaman[1] = RS.getString("NIM");
                dtpeminjaman[2] = RS.getString("NAMA");
                dtpeminjaman[3] = RS.getString("KODE_BUKU");
                dtpeminjaman[4] = RS.getString("JUDUL_BUKU");
                dtpeminjaman[5] = RS.getString("TANGGAL_PINJAM");
                dtpeminjaman[6] = RS.getString("TANGGAL_KEMBALI");
                
                DTM.addRow(dtpeminjaman);
            }
                hitungJumlahPeminjaman();
        }
    }
    
    
